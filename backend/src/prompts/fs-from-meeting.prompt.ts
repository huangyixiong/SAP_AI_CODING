export function buildFSFromMeetingUserMessage(
  meetingContent: string,
  projectContext?: string
): string {
  const contextSection = projectContext
    ? `\n**项目背景补充：**\n${projectContext}\n`
    : '';

  return `请根据以下业务沟通内容生成功能说明书：
${contextSection}
**沟通文档内容：**
${meetingContent}`;
}
