export function buildTSUserMessage(programName: string, objectType: string, sourceCode: string): string {
  return `请为以下 ABAP 程序生成技术说明书：

**程序名称：** ${programName}
**对象类型：** ${objectType || 'PROG'}

**程序源码：**
\`\`\`abap
${sourceCode}
\`\`\``;
}
